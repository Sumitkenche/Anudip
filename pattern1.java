package Anudip;
public class  pattern1
{
    public static void main(String args[])
    {
        int n=5;
        for(int i=n; i>=1;i--)//n to 1
        {
            for(int j=1; j<=i;j++)//n stars to 1 star as per i value
            {
                System.out.print("*");
            }   
            System.out.println(" ");     
    }
        }
        
}

